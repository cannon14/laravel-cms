													<p> <em>Signature Panel</em> <br>
												Always sign the back of your credit card instead of writing 'Check ID' or 'SEE ID'. Your signature signifies that you have accepted the terms of your credit card, without which your card may not be accepted by merchants. </p>
												</li>
										<li>
													<p> <em>Credit card information over the phone</em> <br>
												It is not advisable to provide your credit card information over the phone, unless you have initiated the call and trust the party or retailer.  It is also safer to use corded land line phones rather than cordless or cell phones, as these transmissions can potentially be intercepted. </p>
