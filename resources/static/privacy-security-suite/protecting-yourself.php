								<li> <strong>Online: When applying for a new card or using it</strong> <br />
											<br />
											<ul>
										<li>
													<p> <em>Security</em> <br>
												Always check for the browser's "lock" icon, but understand that this only signifies a secure communication channel, not necessarily the legitimate website of your card issuer. </p>
												</li>
										<li>
													<p> <em>Anti-virus and spyware protection software</em> </p>
													<ul>
												<li>Maintain active, up-to-date anti-virus and spyware protection software. Take the time to learn how to use these tools. Most can be set to automatically update and run at least once a week.</li>
												<li>Keep your operating system and browser updated with the latest security patches.</li>
											</ul>
													<p></p>
												</li>
										<li>
													<p> <em>Password</em> </p>
													<ul>
												<li>Never share with anyone the information used to authenticate your online banking and credit card account(s).</li>
												<li>Keep your password safe - Avoid writing your password information down where others might find it, or in a file on your computer.</li>
												<li>Use a strong password - at least eight characters, with a combination of numbers, upper and lower case letters and other special characters.</li>
												<li>Do not use the same password for financial websites that you use for other online accounts.</li>
												<li>Change your password periodically.</li>
											</ul>
													<p></p>
												</li>
										<li>
													<p> <em>Review your account activity periodically</em> <br>
												Check your credit card account statements monthly for unauthorized activity. If you see activity that does not look familiar, notify your credit card issuer immediately. </p>
												</li>
										<li>
													<p> <em>Accessing your online credit card account</em> <br>
												Access your credit card issuer's website by typing the URL in your browser address bar, rather than clicking links you might see in an email. </p>
												</li>
