			<p>Following the Federal Reserve's unscheduled rate cut two  weeks ago and Fed chief Ben Bernanke's recent warning that further cuts might  be needed, issuers appear to be leaving APRs alone. Meanwhile, credit card  losses continue to hurt bank's earnings. American Express, Capital One and  several regional banks reported third-quarter weakness in their card businesses  as delinquencies increased. &nbsp;</p>
			<p>Retailers are also feeling the impact of soured card loans.  Target said that net write-offs of its uncollected credit card bills topped 10  percent in September, with analysts calling for even higher credit card write-offs  later this year. In a write-off, the card issuer sells the original debt for  pennies on the dollar, assuming a loss and leaving the debt collector to pursue  payment from the debtor. </p>
