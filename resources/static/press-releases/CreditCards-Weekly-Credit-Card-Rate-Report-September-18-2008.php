			<p>Expressing &quot;significant concern&quot; about the threats  from both economic weakness and inflation, the Fed's rate-setting committee  members voted unanimously to hold the federal funds rate at 2 percent. That leaves  an unchanged prime rate, which banks use to set APRs on variable rate  cards.&nbsp;&nbsp; &nbsp;</p>
			<p>However, analysts who spoke to CreditCards.com indicated  that regardless of the Fed's decision, the most likely direction for credit  card APRs is upward, as banks seek to guard their profits against card  delinquencies and general economic weakness. In just the past week, the U.S.  financial sector has been shaken by Lehman Brothers' bankruptcy filing and a government  rescue of troubled insurance giant AIG. </p>
