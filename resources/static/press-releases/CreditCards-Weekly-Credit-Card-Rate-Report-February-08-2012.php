			<p>For a second straight week, the  average annual percentage rate (APR) stayed at 14.91 percent -- its lowest  level since August 2011 when it hit 14.9 percent. This is the eighth  straight week without an increase in the national average APR, the longest such  stretch since CreditCards.com began tracking rates in 2007.</p>
			<p>But creditors aren't just sitting on their hands. Wells Fargo  changed the top end of the APR range for its Cash Back and Rewards cards from  23.15 percent to 25.99 percent. The low end of the range for each card remained  at 12.15 percent. Since only the low ends of ranges are used to calculate  CreditCards.com's national average, Wells Fargo's move did not affect it. </p>
			<p>The CreditCards.com credit card rate survey (permalink: <a href="http://www.creditcards.com/rate-report">http://www.creditcards.com/rate-report</a>)  is conducted weekly, using offer data from the leading U.S. card  issuers' websites. Introductory offer periods and regular interest rates will  vary with applicants' credit quality and issuer risk-based pricing policies. </p>

