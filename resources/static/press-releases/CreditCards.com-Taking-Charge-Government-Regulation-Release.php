			<h1>Taking Charge: Government Regulation Release</h1>
			<h2 style="font-size: 10pt;">More than half say they distrust credit card companies  although eight in 10 say credit cards are essential today</h2>

			<p><strong>Austin</strong><strong>, TX</strong><strong>, Aug. 13, 2008</strong> &ndash; CreditCards.com&rsquo;s  Second Annual <em>Taking Charge</em> survey revealed  today that 73 percent of Americans want the government to regulate the credit  card industry more closely; and 58 percent of Americans expressed a&nbsp; distrust of credit card companies. Most  consumers (78 percent), however, acknowledge that many people make little  attempt to understand the credit card information they have and do not read the  terms of their credit card offers.</p>
			<p>The national  poll is an annual snapshot of America&rsquo;s relationship with credit cards and was conducted  by GfK Roper Public Affairs &amp; Media for CreditCards.com, the leading online  credit card marketplace that helps consumers make smart credit card choices.  (Read the complete article at <a href="http://www.creditcards.com/regulate">www.creditcards.com/regulate</a>.)</p>
			<p>&ldquo;The <em>Taking Charge</em> results illustrate the public&rsquo;s hostility towards the credit card industry,&rdquo;  said Ben Woolsey, Director of Marketing and Consumer Research for  CreditCards.com. &nbsp;&ldquo;Amidst the hostility,  however, 82 percent of Americans still say credit cards provide a valuable  service and are essential to have today.&rdquo;</p>
			<p>According  to Woolsey, the negative effects of recent years&rsquo; underwriting standards have  cast a spotlight on the responsibilities that credit lenders have to their  borrowers.&nbsp; Though legislation like the  Credit Cardholder&rsquo;s Bill of Rights continues to advance in Congress, Woolsey urges  consumers to do their part to learn more about credit card issues in order to  better understand their current rights and obligations as card holders.</p>
			<p>The <em>Taking Charge</em> survey also showed that:</p>
			<ul type="disc">
			  <li>82       percent of Americans somewhat agree or strongly agree that credit cards       provide a valuable service.</li>
			  <li>76       percent feel there's always a &quot;catch&quot; that makes a credit card       less attractive than the credit card company makes it sound.</li>
			  <li>56       percent have been disappointed by a credit card that turned out not to be       as good as they expected.</li>
			  <li>78       percent say no one really reads the terms and conditions when they sign up       for a credit card.</li>
			  <li>A       majority of people (58 percent) don't trust credit card companies.</li>
			</ul>
			<p>&nbsp;</p>
			<p>These <em>Taking Charge </em>results are the first of a three-part series.&nbsp; Subsequent findings on America&rsquo;s  relationship with credit cards will be released August 20 and August 27.</p>
