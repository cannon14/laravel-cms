			<p>For the 13th week in a row, the national average annual percentage rate remained glued at 15 percent after credit card issuers declined to change interest rates on advertised cards. Among the 100 cards monitored weekly by CreditCards.com, not a single card advertised a new rate or annual fee this week.</p>

			<p>Interest rates have remained in place for a record number of weeks this year, making comparing rates more predictable. Rates on existing credit cards could change soon, however, if the Federal Reserve announces a federal funds rate increase at its Thursday Federal Open Market Committee meeting. A potential rate increase would affect variable rate cards, but offer rates that make up the Rate Report are not directly affected.</p>

