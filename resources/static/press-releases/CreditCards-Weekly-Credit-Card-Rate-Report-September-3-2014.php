			Most credit card issuers left interest rates alone. The sporting goods store Cabela's reduced the lowest available APR on the Cabela's Club Visa from 15.15 percent to 15.14 percent. But the change was too small to affect the national average. Cabela's also lowered the maximum available APR to 21.14 percent.</p>
