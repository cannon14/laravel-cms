			<h1>The Last Taboo: Americans Will Talk about Sex Before Credit Card Debt</h1>

			<p><strong>Austin, TX, July 8, 2008</strong> -- A consumer information poll today reported that that Americans are more likely to talk about their weights, ages, religious views and even their health problems, than they are willing to talk about credit card debt.</p>
			<p>According to the poll, conducted by GfK Roper Public Affairs and Media for CreditCards.com, the leading online source for consumer credit card information, eight out of 10 adults (82 percent) are reluctant to openly discuss their credit card debts with someone they just met.
			<p>The CreditCards.com poll found that details of your love life were second, with 78 percent of respondents saying they were somewhat or highly unlikely to broach the topic of their personal relationship. Respondents were also unlikely to discuss: salary (77 percent), monthly mortgage or rent payments (69 percent), health problems (58 percent), the death of a loved one (49 percent), your weight (47 percent), views about religion (37 percent), political views (36 percent), your age (26 percent), the price of a tank of gas (13 percent) and the weather (8 percent).</p>
			<p>"We're surprised to find that people are more comfortable talking about the death of a loved one than discussing their credit card debt," said Ben Woolsey, Director of Marketing and Consumer Research for CreditCards.com. "Talking about credit card debt is an overwhelming social taboo. There is a social paradox happening -- people who are faced with credit card debt are unwilling to face their financial issues and therefore may be leaving it unresolved."<br>
			In addition, the CreditCards.com poll also found:<br>
			</p>
			<ul>
			<li>Nearly a third (35 percent) of Americans say they carry a balance on their credit cards, while 51 percent say they do not, 10 percent do not have credit cards and 5 percent did not know if they had balances.</li>
			<li> Even those who say they do not carry a balance were unwilling to discuss their credit card debt, only 20% of this group indicated they would openly discuss the topic, compared to 16% for those who carried a balance.</li>
			<li> More Americans with higher household incomes admit to carrying a balance (44% of those with household income of $50k+ vs. 30% of those with lower incomes).</li>
			<li> Aside from discussing their love life, three of the top four topics people were least likely to talk about with someone they met for the first time were about finance: personal credit card debt, salary and mortgages.</li>
			<li> Men are more likely to discuss credit card debt (77 percent) than women (83 percent) as well as their love life, age, weight and political views.</li>
			</ul>
