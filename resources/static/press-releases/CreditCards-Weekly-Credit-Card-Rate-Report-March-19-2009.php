			<p>With its federal funds rate already near zero, the Fed cannot  lower interest rates through traditional monetary policy. Instead, in an effort  to encourage lending, the Fed will purchase up to $300 billion of longer-term  Treasury securities and introduce the Term Asset-Backed Securities Loan  Facility (TALF). Experts say both those steps have the potential to lower  credit card APRs. </p>
			<p>Still, analysts say that rising unemployment -- and the  resulting threat of credit card losses as consumers fail to make payments --  means banks will be unlikely to provide any rate relief to cardholders. Despite  a historically low fed funds rate, APRs have increased five out of the last six  weeks. </p>
