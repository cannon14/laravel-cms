			<p>Since January, the national average annual percentage rate  (APR) has changed just 16 times, often by just a hair.</p>
			<p>Each time the national average has shifted, the movement has  been by less than half of a percentage point. As a result, average rates have  hovered within rounding distance of 15 percent for the past two years. </p>
			<p>In fact, average rates are so sticky these days, the current  average APR is just two-hundredths of a percentage point shy of where it was  during the same week in 2011, when average rates hit 14.98 percent. Two years  ago, average rates were just slightly lower, at 14.63 percent. </p>
