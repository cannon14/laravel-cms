			<p>Chase spurred this week's  rate change by hiking the APRs on two of its travel rewards cards. Chase boosted  the APR on the United MileagePlus Explorer card from 15.24 percent to 15.99  percent.</p>
			<p>It made the same change to  the IHG Rewards Club Select credit card, which also charged cardholders a  single APR of 15.24 percent. Applicants who apply for the hotel rewards card  will now be charged a flat APR of 15.99 percent.</p>
