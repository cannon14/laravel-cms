			<h1>Taking Charge: America's Relationship with Credit Cards</h1>

			<p>
			<strong>CreditCards.com Sponsors Benchmark Study Which Reveals Disconnect between Americans' Credit Card Knowledge and Usage</strong>
			</p>
			<p>
			AUSTIN, Texas--(BUSINESS WIRE)--More than 8 in 10 Americans view their credit cards as "an easy way to get into debt," according to the new report <strong>Taking Charge: America's Relationship with Credit Cards</strong>. The benchmark report delves into every aspect of credit cards' role in people's lives: how Americans use, manage, understand, select, and feel about credit cards.
			</p>
			<p>
			<strong>Taking Charge: America's Relationship with Credit Cards</strong> was fielded by GfK Roper Public Affairs and Media and sponsored by CreditCards.com, a leading online resource for credit card information. The national study, conducted by phone, surveyed 1,000 Americans about the plastic in their pockets.
			</p>
			<p>
			"Just like interpersonal relationships, Americans' relationships with their credit cards are both passionate and conflicted," explains Elisabeth DeMarse, CEO of CreditCards.com. "Credit cards, virtually ubiquitous modern accessories, play a fundamental and often misunderstood role in people's lives. This milestone study examines America's personal finance decisions, exploring the contradictions between how the public thinks about credit cards and how they actually use them."
			</p>
			<p>
			By some estimates, the average American household has over $9,300 in credit card debt. Yet, despite Americans' concern about their spending habits, few people are willing to own up to their balances: over 90 percent of survey respondents believe they had the same amount or less debt as the average American. This makes a revealing statement about America's complex relationship with credit cards.
			</p>
			<p>
			<strong>Love-Hate Relationship: Credit cards are a gateway to debt but are just so darn convenient</strong>
			</p>
			<p>
			Americans overwhelmingly see credit cards as "debt" cards. When asked how they felt about credit cards, respondents were over five times as likely to characterize cards as "an easy way to get into debt" than they were to describe them as "empowering" or an "indispensable part of life." More than a third admit to using their cards for <strong>purchases they can't afford</strong>. A telling response in the survey's qualitative section described a card as "plutonium in my pocket."
			</p>
			<p>
			Despite this, the same respondents embrace credit cards for their <strong>convenience</strong>, with over 60 percent saying it's their primary reason for using their card. Across all demographic groups gender, age and income bracket  Americans say they swipe their card because it's easy.
			</p>
			<p>
			<strong>Lack of Knowledge: Americans don't know basic credit card facts and aren't trying to learn</strong>
			</p>
			<p>
			Over 9 in 10 Americans do not know how long it would take to pay off a credit card bill if one were to make just the <strong>minimum payment</strong>. Asked how long it would take to pay off a $1,000 bill, over half underestimated the actual time (7-8 years). What's more, almost three in four say they don't bother reading the terms and conditions of their own credit cards.
			</p>
			<p>
			Americans are also woefully misinformed about what actions to take to enhance their <strong>credit ratings</strong>. The majority of respondents believe that it would be financially prudent to have no debt, close credit card accounts, and have unused credit cards. In reality, all three actions can hurt, not help, personal credit ratings.
			</p>
			<p>
			<strong>Studied Avoidance: When people do know what's best they ignore their own advice</strong>
			</p>
			<p>
			Even when Americans can name the financially responsible strategy for managing their cards, they rarely follow through. Fully 72 percent of respondents realize that they should <strong>consolidate their balances</strong> onto a single card but only 22 percent actually have done so. Likewise, nearly 80 percent advocated checking their credit score, but only half said they occasionally do so.
			</p>
			<p>
			<strong>Unsatisfied Apathy: Americans dislike the card they have but don't search for a new one</strong>
			</p>
			<p>
			Over half of credit card owners feel they are living with a card that's not right for them. Yet, they don't like any of their options for finding a new one. Americans are universally dismissive of the offers they receive in the mail, with nearly 90 percent saying they shred the promotional offers or throw them away.
			</p>
			<p>
			Only one in five has actively searched online for the credit card best suited to their needs. Worse, Americans admit that they spend more time comparison-shopping for everything from electronics to cell phone plans than they do reviewing credit card enrollment terms.
			</p>
			<p>
			"There is enormous potential for Americans to take charge of their own personal finance habits and presumptions and transform their credit cards from a liability into a powerful tool and ally," DeMarse observed.
			</p>
			<p>
			<strong>About Taking Charge: America's Relationship with Credit Cards</strong>
			</p>
			<p>
			The findings presented are the results of 1,000 phone surveys conducted from March 5, 2007, to March 12, 2007, by Roper Public Affairs and Media, a part of GfK NOP. Interviews were conducted among a nationwide representative sample of 1,000 adults age 18 and older in the continental United States. Data were weighted for sample balancing by sex and age. The margin of error for the total sample is +/- 4.4 percentage points at the 95 percent confidence level. The margin of error for subgroups is higher. Because of rounding, numbers may not total 100 percent.
