			<p>Unlike previous rate changes, however, this week's change was due to a reshuffling of the CreditCards.com database rather than a terms change.</p>
			<p>Citi is no longer offering the Citi Dividend Card for College Students online or over the phone. The card was replaced in the database with a similar cash-back card: the Discover "it" Chrome for Students card. The new Chrome card was introduced to the market earlier this year and advertises a slightly lower minimum APR of 12.99 percent. The card also features a 0 percent promotional APR for 6 months.</p>
