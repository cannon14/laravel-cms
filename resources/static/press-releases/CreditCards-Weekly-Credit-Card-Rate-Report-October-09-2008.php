			<p>The Fed's action, cutting the fed funds rate to 1.5 percent,  was extraordinary in two ways -- it came outside the Fed's regular meeting  schedule, and it was part of a coordinated set of rate cuts by central bankers  worldwide.</p>
			<p>When the Fed cuts the fed funds rate, it usually cuts  borrowing costs for people who carry a balance on variable rate cards. Those  cards' rates are pegged to the prime rate, which moves in step with the prime.<br>
			&nbsp;<br>
			That may not be true this time. Many cards have hit  &quot;floor&quot; rates beyond which they will not fall, and most issuers are  under financial pressure themselves and may adjust their terms to keep the rate  cut for themselves.</p>
