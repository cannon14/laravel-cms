			  <p>Most card issuers left interest rates alone this week. The  sporting goods store Cabela's readjusted the APR on the Cabela's Club Visa by  0.01 percent. However, the rate change was too small to affect the national  average.</p>
              <p>Frequent Cabela's shoppers who want to use the card for  non-Cabela's store purchases may now qualify for an APR between 15.14 percent  and 21.14 percent.</p>
              <p>Cabela's revised the store card's APR after the one-month  LIBOR rate decreased from 0.16 percent to 0.15 percent. Unlike most U.S. credit  cards, which are tied to the U.S. prime rate, the Cabela's Club Visa is tied to  the 30-day LIBOR rate. When the one-month LIBOR rate falls, the APR on the  Cabela's Club Visa automatically declines as well.</p>
The CreditCards.com credit card rate survey  (permalink: <a href="http://www.creditcards.com/rate-report">http://www.creditcards.com/rate-report</a>)  is conducted weekly, using offer data from the leading U.S. card issuers'  websites. Introductory offer periods and regular interest rates will vary with  applicants' credit quality and issuer risk-based pricing policies.
