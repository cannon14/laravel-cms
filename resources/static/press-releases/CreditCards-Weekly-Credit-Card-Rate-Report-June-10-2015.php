				<p>Average card rates held steady at 14.99 percent.</p>
				<p>Most issuers left card offers alone this week. However, Barclaycard recently cut off applications for the Visa Black card, but the move is only temporary, according to Barclaycard spokeswoman Nicole Dye-Anderson. "We've temporarily closed acquisition channels while we work to enhance the product," said Dye-Anderson in an email. "We have a lot of great things coming up."</p>
				<p>To reflect the interim closing, CreditCards.com removed the Visa Black card from the CreditCards.com database and replaced it with the Citi Prestige card, which charges a slightly higher APR. The switch resulted in some category APR change, but didn't affect the national average. </p>

