			<p>Banks continue to adjust their card offers before major  provisions of the landmark Credit CARD Act take effect on Feb. 22. That  consumer friendly legislation will, among other things, restrict banks' ability  to adjust card terms.&nbsp;&nbsp; </p>
			<p>Ahead of the law, Bank of America increased the APR on one  of its cards and Discover raised the top end of the APR range on one of its  cards. Other terms and conditions are being tweaked as well. For example, four  cards eliminated or shortened introductory balance transfer APR periods.</p>
			<p>The CreditCards.com credit card rate survey is conducted  weekly, using offer data from the leading U.S. card issuers' Web sites. Introductory  offer periods and regular interest rates will vary with applicants' credit  quality and issuer risk-based pricing policies. </p>
