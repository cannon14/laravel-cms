						<p>None of the cards tracked by  CreditCards.com advertised new interest rates. As a result, the national  average annual percentage rate (APR) remained at 14.9 percent for the fourth  week in a row.</p>
						<p>Most card offers have  remained unchanged since the beginning of the year. Out of 100 cards, just  eight cards feature new rates this year.</p>
						<p>Three cards saw their minimum  rates increase. An equal number have enjoyed a rate cut on their lowest  available APR.</p>
						<p>Pentagon Federal Credit Union  made the most dramatic rate change this year: It slashed the minimum APR on the  PenFed Promise card from 9.99 percent to 7.99 percent. It also increased the  card's maximum APR by 7 percentage points -- from 9.99 percent to 16.99  percent.</p>
The CreditCards.com credit card rate survey  (permalink: <a href="http://www.creditcards.com/rate-report">http://www.creditcards.com/rate-report</a>) is conducted weekly, using offer data from the  leading U.S. card issuers' websites. Introductory offer periods and regular  interest rates will vary with applicants' credit quality and issuer risk-based  pricing policies.
