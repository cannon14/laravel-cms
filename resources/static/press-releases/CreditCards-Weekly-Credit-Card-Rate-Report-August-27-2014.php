			<p>The national average annual percentage rate (APR) inched up to 15.06 percent Wednesday after remaining at 15.03 percent for seven consecutive weeks.<br>
			<br>
			Wells Fargo prompted this week's rate change by hiking APRs on two of its rewards credit cards. Wells Fargo increased the lowest available APR on the Wells Fargo Rewards Card from 12.15 percent to 14.15 percent. It left the card's maximum available rate alone at 25.99 percent.<br>
			<br>
			The issuer also narrowed the range of possible APRs for the Wells Fargo Cash Back Card by increasing the lowest available APR by 1 percentage point. Applicants may now qualify for an APR as low as 13.15 percent or as high as 25.99 percent, depending on their creditworthiness.<br>
