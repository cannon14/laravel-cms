			<p>More than a week after the Federal Reserve increased the federal funds rate by 0.25 percent, Citi hiked APRs on its credit cards by the same amount. As a result, the national average APR rose to 15.07 percent -- one of the highest average APRs ever recorded by the CreditCards.com Weekly Rate Report.</p>

			<p>The last time average rates were this high was in late 2014.</p>

			<p>Card issuers are acting quickly to pass along the Fed rate hike to their customers with variable rate cards, as allowed under federal law. Among the 100 cards tracked by CreditCards.com 34 have increased rates since the Fed's action, all by the same quarter point. </p>

            <p>The CreditCards.com credit card rate survey (permalink: http://www.creditcards.com/rate-report) is conducted weekly, using offer data from leading U.S. card issuers' websites. Introductory offer periods and regular interest rates will vary with applicants' credit quality and issuer risk-based pricing policies.</p>

