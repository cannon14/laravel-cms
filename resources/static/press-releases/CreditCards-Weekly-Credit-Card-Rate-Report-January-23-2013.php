			<p>The national average annual percentage rate (APR) fell to  14.95 percent Wednesday. This is the first time since November that average  rates have changed. Unlike previous weeks, however, the change was due to a  reshuffling of the CreditCards.com database, rather than to a new APR on an  existing card.</p>
			<p>CreditCards.com occasionally replaces old cards with new  ones in order to more accurately reflect the current card market. This week, we  swapped two Citi cards that are no longer available online, the Citi Forward  card and the Citi Platinum Select Visa, with two cards that have similar  features: the Citi Thank You Preferred card and the Discover &quot;it&quot;  card. The slight difference in the cards&rsquo; APRs caused the national average to  decline.</p>
			The CreditCards.com credit card rate survey  (permalink: <a href="http://www.creditcards.com/rate-report">http://www.creditcards.com/rate-report</a>)  is conducted weekly, using offer data from the leading U.S. card issuers'  websites. Introductory offer periods and regular interest rates will vary with  applicants' credit quality and issuer risk-based pricing policies.
