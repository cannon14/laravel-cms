			<p>For the fourth straight week, the average annual percentage rate (APR) stood at 14.91 percent, its lowest level since August 2011. It's the sixth time in the first eight weeks of the year that rates have gone unchanged.</p>
			<p>When rates have moved in 2012, they have fallen -- once in January and once in early February. Dating back to late 2011, we have gone 10 straight weeks without seeing a week-to-week increase in the national average APR -- the longest such stretch since CreditCards.com began tracking rates in 2007.</p>
			<p>The last increase we saw was in mid-December, as rates jumped from 14.98 percent to a record 15.22 percent. That increase was the largest in more than a year.</p>
