			<p>The national average annual percentage rate (APR) remained  at 14.94 percent Wednesday, after increasing by just one basis point the  previous week.</p>
			<p>Last week marked the first time in three months that average  rates increased.</p>
			<p>Average interest rates have remained exceptionally stable through  much of 2013, thanks to issuers leaving most card offers alone. For example, since  Jan. 1, the national average has remained the same 15 weeks out of 20.</p>
			<p>Average rates increased just twice since the beginning of  the year and have fallen three times -- each time by just a small amount.</p>
			<p>As a result, the national average is currently just 0.02  percent below where it was at the beginning of 2013.</p>
