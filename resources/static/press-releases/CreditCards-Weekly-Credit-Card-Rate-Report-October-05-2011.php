			<p>The average annual percentage rate (APR) stayed at a record 14.97 percent
			for a second straight week.&nbsp; </p>
			<p>This week's only APR change was from Cabela's. The sporting goods
			retailer changed the top end of the APR range offered for the Cabela's
			Club Visa from 18.21 percent to 18.23 percent. The low end of the
			range, 9.99 percent, stayed unchanged. However, we only use the
			low end of ranges in our calculations, so the move did not affect
			the national average.</p>
			<p>Cabela's chief financial officer Kevin Werts attributed the move
			to a change in the London Interbank Offered Rate (Libor), the British
			equivalent of the U.S. federal funds rate. When Libor moves, all
			cards tied to it move in the same direction and by the same amount.<br>
