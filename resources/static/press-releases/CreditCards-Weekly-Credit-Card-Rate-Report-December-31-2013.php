			<p>The national average annual percentage rate (APR) remained  at 15.06 percent Tuesday for the seventh straight week.</p>
			<p>None of the cards tracked by CreditCards.com advertised new  interest rates. Promotional terms, including introductory APRs and balance transfers,  remained unchanged as well.</p>
			<p>Credit card issuers have been wary of dramatically changing  rates throughout 2013. As a result, the national average APR moved up by just  one-tenth of a percentage point between Jan. 2 and Dec. 31.</p>
			<p>The average APR for the year increased by just 0.02 percent  between 2012 and 2013 -- underscoring just how little card APRs have changed  over the past two years. In 2012, for example, average rates hit 14.96 percent.  In 2013, they finished just slightly higher at 14.98 percent.</p>
