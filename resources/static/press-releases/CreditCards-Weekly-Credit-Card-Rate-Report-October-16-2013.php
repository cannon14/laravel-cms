			<p>The national average annual percentage rate (APR) remained  at 15.03 percent Wednesday after ticking up the previous week for the third  time in two months.</p>
			<p>None of the cards tracked by CreditCards.com advertised new  rates this week. Promotional APRs and balance transfer offers also remained  unchanged.</p>
			<p>The majority of card issuers have left interest rates alone  for much of the past year. However, some issuers, including Capital One and  Barclays, have recently hiked rates on a select number of cards. As a result,  the national average has increased somewhat since August.<br>
