			<h1>Be Prepared for Arguments and Snooping When Sharing Credit Card Accounts</h1>

			<p><strong>CreditCards.com Poll Finds Shared Credit Card Accounts,  Dissatisfaction with Credit Cards Are Sources of Anxiety</strong></p>
			<p><strong>Austin</strong><strong>, Texas</strong><strong>, Aug. 27, 2008</strong> &ndash; Nearly one in five  people who share credit card accounts say use of the accounts have sparked  arguments with the other person, according to a new CreditCards.com poll. The amount  of personal conflict greatly increases when shared account holders are  dissatisfied with their current credit card.&nbsp; </p>
			<p>The findings are from the Second Annual <em>Taking Charge</em> survey, which investigates America&rsquo;s relationship with credit  cards.&nbsp; The national study was fielded by  GfK Roper Public Affairs &amp; Media for CreditCards.com, the leading online  credit card marketplace and consumer information source.&nbsp; </p>
			<p>&ldquo;Sharing a credit card account can be risky if the other  person is irresponsible in their spending. Our survey shows there is some  concern about this practice and how it affects relationships,&rdquo; said Ben  Woolsey, Director of Marketing and Consumer Research for CreditCards.com.&nbsp; &ldquo;Whether it's your children or your spouse or  partner, you should really be careful about sharing a credit card account. </p>
			<p>&quot;Results also show that people's perception of having  the right credit card for their needs makes a difference in their  happiness,&quot; Woolsey added. &quot;Americans sensing a disconnect between  themselves and their credit card typically double their likelihood of  experiencing relationship discord on shared credit card accounts.&rdquo; </p>
			<p>The CreditCards.com <em>Taking  Charge</em> survey also finds:<br>
			</p>
			<ul type="disc">
			<li>More than half (51 percent) of cardholders have shared a credit card account,       mostly with a spouse or partner (91 percent) and some with a child or       adult child (21 percent).&nbsp; </li>
			<li>Sharing an account makes 9 percent of the shared account holders feel closer to       the other person.</li>
			<li>Nearly one-fifth of shared account holders have used printed statements to check       on the other person&rsquo;s spending; and nearly one-sixth (15 percent) have       checked statements online. </li>
			<li>Nearly one in five (17 percent) of shared account holders said they were       concerned that their own credit scores would be negatively affected by the       other person's use of the account.</li>
			<li>A few shared account holders (7 percent) have canceled credit cards because they       caused conflict in their relationships. </li>
			</ul>
			<p>In addition, the poll shows the value of shopping around for  a card that matches your needs. People sensing a misfit between themselves and  their credit card are:</p>
			<ul type="disc">
			<li>Twice as likely to have argued with their spouse, partner or child about their       shared account (31 percent versus 16 percent).</li>
			<li>Twice as likely to have printed account statements to monitor the spending       habits of a spouse, partner, child or adult child (34 percent versus 18       percent).</li>
			<li>Three times as likely to worry that their own credit rating will be adversely       affected by their partner&rsquo;s credit habits (37 percent versus 12 percent).</li>
			</ul>
