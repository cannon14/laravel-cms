			<p>American Express' increases offset a decline in APRs  elsewhere. In the wake of the Federal Reserve's late October rate cut, some major  card issuers, including Citi and Chase, lowered APRs this week on variable rate  cards tied to the prime rate, which tracks the Fed's key lending rate. However,  those changes paled in comparison to AmEx's moves. </p>
			<p>Further Fed rate cuts could be pending. In a speech Wednesday,  Fed Vice Chairman Donald Kohn said that with financial markets remaining &quot;impaired  in many ways,&quot; the Fed must continue to consider further steps to &quot;to  re-open credit flows and support the economy.&quot; Those comments, coupled  with news that jobless claims reached a seven-year high last week, suggest the  Fed could again lower rates when it meets in December. </p>
