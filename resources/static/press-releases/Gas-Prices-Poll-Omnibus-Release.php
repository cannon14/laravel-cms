			<h1>CreditCards.com Poll: More than Half of Americans Have Cut Back on Visiting Family, Friends Because of High Gas Prices</h1>

			<p><strong>Austin, TX, May 27, 2008</strong> - A national poll released today shows that more than half of American drivers are curbing visits to friends and family due to rising gasoline costs.</p>
			<p>
			According to the poll, conducted by GfK Roper Public Affairs and Media for CreditCards.com, the leading online source for consumer credit card information, four out of five Americans said that the gas crisis has caused them to cut back their driving in some way.</p>
			<p>
			The CreditCards.com poll found that 53 percent of Americans have decreased driving to visit friends or family; 66 percent think twice before going on a pleasure drive; 54 percent have cut back on driving to vacation destinations and 56 percent of drivers are running fewer errands than usual. One in four people say gas prices are forcing them to curtail driving to work.</p>
			<p>
			"We're seeing a social change emerging from the economic constraints of high fuel costs," said Ben Woolsey, Director of Marketing and Consumer Research for CreditCards.com. "Restructuring driving habits is more than a personal finance issue. It is affecting the American lifestyle: less extracurricular activities for the kids, more planning of daily errands and fewer leisurely drives to enjoy the day or to getaway for the weekend."</p>
			<p>
			In addition, the CreditCards.com poll also found:<br>
			</p>
			<ul>
			<li>Residents of the South (58 percent) are more likely than people from other regions to say gas prices have caused them to cut back on visits to family members and friends.<br>
			    </li>
			<li>Older adults are least likely to give up visits to family members and friends because of gas prices. Only 44 percent of people 65 and older say they cut back on these outings because of gas prices. Nearly 59 percent of people 18 to 34 years old and 57 percent of those 35 to 49 years old say they have cut back on family and friend visits.<br>
			</li>
			<li>The groups reporting the most cutbacks on family vacations are those earning $30,000 to $49,900 a year, young adults and those living in the South.<br>
			</li>
			<li>Americans with lower household incomes (under $30,000) are more likely to say they are cutting down on their drive to work, compared to their higher income counterparts.<br>
			</li>
			<li>Younger adults (ages 18 to 34) have cut back on driving to work more than any of their older counterparts.<br>
			</li>
			<li>People living in the Northeast were least likely to say they have cut back on driving due to gas prices.</li>
			</ul>
