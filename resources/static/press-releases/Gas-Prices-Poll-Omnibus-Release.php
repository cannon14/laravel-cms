			<p><strong>Poll Methodology </strong><br/>
			The survey was conducted from May 16-18, 2008, by GfK Custom Research North America on behalf of CreditCards.com, via random digit dialing phone interviews with 1,004 interview subjects. Interviewees were approximately split between males and females ages 18 and over, with 523 females and 481 males surveyed. The raw data was then weighted by a custom designed computer program that automatically developed a weighting factor for each respondent, employing five variables: age, sex, education, race and geographic region. The total margin of error on weighted data for the full sample is plus or minus 3 percentage points at the 95 percent confidence level.</p>
