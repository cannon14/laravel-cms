			<p>The national average annual percentage rate (APR) on new  card offers remained at 14.97 percent Wednesday.</p>
			<p>None of the cards that CreditCards.com tracks featured offer  changes this week. That includes changes to promotional balance transfer offers  and short-term promotional APRs.</p>
			<p>The lack of change is nothing new, however. Back-to-back  weeks in which card offers did not change have become the norm this year. For  example, this week marks the 20th time this year that average interest rates  haven&rsquo;t budged.</p>
			<p>In the rare instances that average rates have increased or  declined, the changes have been nominal. The last time average rates changed by  close to a full percentage point from one week to the next was in January  2010.</p>
