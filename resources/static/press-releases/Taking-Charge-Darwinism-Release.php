			<h1>Financial Darwinism: National Survey Finds Society is Adapting Credit Card Behavior to Meet Current Market Conditions</h1>

			<p><strong>Austin</strong><strong>, TX</strong><strong>, August 20, 2008</strong> &ndash; Call it  survival of the financially fittest, or financial Darwinism. Consumers are  adapting to a changed economic landscape by cutting back on their credit card  spending, attempting to pay down debt and monitoring interest rates, according  to a new nationwide poll from CreditCards.com.</p>
			<p>The findings are from the Second Annual <em>Taking Charge</em> survey, an in-depth investigation of America&rsquo;s relationship  with credit cards. The national study was fielded by GfK Roper Public Affairs  &amp; Media for CreditCards.com, the leading online credit card marketplace and  consumer information source. </p>
			<p>Taken as a whole, the poll shows most consumers are reacting  reasonably to financial difficulties by pulling back on discretionary spending,  and express little concern about their ability to pay down debt. But those who  can't or won't adjust are finding themselves in deeper and deeper debt trouble.  To put it in Darwinian terms, consumers must adapt or they simply won't  financially survive.</p>
			<p>&ldquo;As the use of plastic becomes more prevalent in American  daily life, consumers who implement healthy credit card habits will be more apt  to financially survive in a cashless society,&rdquo; said Ben Woolsey, Director of  Marketing and Consumer Research for CreditCards.com. &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;</p>
			<p>Consumers appear to be taking proactive steps to avoid the  credit crunch in their credit card affairs:</p>
			<ul>
			<li>25 percent of U.S. cardholders say they spent  less on household living expenses last month compared to normal. </li>
			<li>One-third of cardholders report that they ended  the previous month with a lower outstanding balance than the month before. </li>
			<li>34 percent say they spent less on discretionary  expenses, such as eating out or shopping at the mall; 46 percent say they spent  less on major purchases, such as appliances or furniture.</li>
			<li>Some consumers are giving up on credit: The  proportion claiming to be current cardholders has dropped 4 percentage points  since the 2007 &quot;Taking Charge&quot; survey. </li>
			<li>Only 7 percent of Americans say they worry  &quot;a lot&quot; about paying off their credit card bills; 63 percent say they  are not at all worried. </li>
			<li>People are hunting for low interest rates.  Nearly 4 in 10 surveyed agreed that they are always looking for low interest  rates to transfer their existing balances, while 67 percent of cardholders  agree that they &quot;actively monitor&quot; the interest rates on their credit  cards.</li>
			<li>Despite all the bad economic news of the past  year, American cardholders are no more worried about paying their credit card  bills, with 27 percent agreeing with the statement, &quot;At times I worry  about how I'm going to pay my credit cards.&quot; That figure is unchanged from  last year's poll. </li>
			</ul>
			<p>Who does worry about paying credit card bills? According to the poll, those  with the greatest credit card bill worries are people: </p>
			<ul type="disc">
			<li>With household incomes below       $75,000.</li>
			<li>Who have four or more credit       cards.</li>
			<li>Who are older than 40.</li>
			</ul>
