			<p>None of the issuers tracked  by CreditCards.com altered interest rates this week. Issuers left promotional  balance transfer offers and introductory purchase rates alone as well.</p>
			<p>This is the 25th week this  year that the national average APR hasn't budged. Credit card issuers tinker  with interest rates so rarely these days that the national average has changed just  seven times since Jan. 1.</p>
			<p>Four of those changes were  due to just one issuer. Between April 2014 and July 2014, Chase increased the  APR on a number of travel rewards cards, including the Southwest Airlines Rapid  Rewards Plus card and the Disney Rewards Visa.</p>
