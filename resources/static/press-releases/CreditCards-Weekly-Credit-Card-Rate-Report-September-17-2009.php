			<p>However, Bernanke added that the economic rebound is  unlikely to produce many jobs. Unemployment has threatened borrowers' ability  to make payments, with credit card issuers reporting they continued to deal  with delinquent cardholders last month. Analysts have noted that banks are, in  part, raising APRs and fees to offset these potential losses.</p>
