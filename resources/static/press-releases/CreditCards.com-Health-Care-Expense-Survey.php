			<h1>62 percent of Americans believe illness could pose financial hardship</h1>

			<p><em>CreditCards.com poll finds Americans are falling into credit card<br> debt to pay for health care</em></p>

			<p>
				<strong>Feb. 1 2008</strong> - Nearly two out of  three Americans (62 percent) say they or their family could face hard financial  times if they experience a serious illness, a new national CreditCards.com poll  finds. <br>
				The poll, conducted for CreditCards.com by GfK Roper Public Affairs & Media, surveyed 1,004 American adults in a random telephone survey Jan. 18-20, 2008.  It is part of a newly released special report, "Health Care and  Credit," in which CreditCards.com takes an in-depth look at how Americans  increasingly turn to credit and fall into debt to pay for health care.<br>
				The CreditCards.com poll found:
			</p>

			<ul>
				<li>Nearly two-thirds of Americans (62 percent)  agree that either they or their family would face a financial hardship if they  experienced a serious illness. Women (66 percent) and lower income families  earning less than $30,000 (73 percent) are even more likely to feel this way. </li>
				<li>If hit with medical bills greater than $1,000,  nearly half (49 percent) of Americans say they would tap savings or checking  accounts to pay their debts. Those with less income, however, were far less  likely to see savings as a choice for health care debt.</li>
				<li>More than a third of respondents (38 percent)  would resort to some form of borrowing to pay medical debts, either through an  installment payment plan (10 percent), a credit card (9 percent), borrowing  from friends or relatives (8 percent), a short-term loan from a bank or credit  union (8 percent) or borrowing from 401(k) retirement accounts (3 percent).</li>
				<li>Only 3 percent say their health insurance would  fully cover a sudden $1,000 medical bill.</li>
				<li>Ignorance is widespread on insurance  deductibles: 40 percent of Americans say they know the amount; 50 percent admit  not knowing and the rest weren't sure or declined to answer.</li>
			</ul>

			<p>In addition to the poll, the special report examines how the health care  industry is changing as out-of-pocket medical costs rise dramatically for many  American families. </p>

			<ul>
				<li>Borrowing to pay for basic health care needs is  becoming a way of life for many Americans and mounting medical debt may be  threatening to bankrupt more families. </li>
				<li>As out-of-pocket health care costs rise for many  families, credit cards, debit cards and gift cards featuring more lenient  financing terms are emerging as health care payment options.</li>
				<li>The new generation of specialty health care  credit cards have 0 percent interest rates and limited repayment periods that  encourage consumers to pay the debts off quickly.</li>
				<li>Consumer advocates say these developments show  how dire things have become in the health care system.</li>
			</ul>

			<p>
				"This is an issue that hits home for almost every American family as  they try to figure out how to pay medical bills," said CreditCards.com Vice  President of Strategic Marketing Jody Farmer. "Many people are putting  medical expenses on credit cards, but consumers must weigh their choices  carefully and comparison shop for the best options. CreditCards.com empowers  consumers seeking information with facts, advice and choices so that they can  make smart credit decisions in these situations."<br>
				The CreditCards.com report also provides 15 tips for paying high medical  bills - practical advice for consumers who are trying to figure out how best to  cope. Among the suggestions: try negotiating with the doctor or hospital to  lower the bill, ask about charity care programs at hospitals and work out  payment plans with the medical provider.<br>
