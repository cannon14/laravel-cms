			<h1>CreditCards.com Recognized for Journalistic Excellence</h1>

			<p>CreditCards.com, the Internet&rsquo;s leading credit card comparison website,
			has won two &ldquo;Best in Business&rdquo; awards from the largest and
			most prestigious business journalism organization in the United States,
			the Society of American Business Editors and Writers (SABEW).&nbsp; </p>
			<p>The first award honored a CreditCards.com special project that examined
			the difficulty consumers have in reading and understanding the average
			credit card agreement. The &ldquo;<a href="http://www.creditcards.com/credit-card-news/credit-card-agreement-readability-1282.php">Credit
			Card Readability Project</a>,&rdquo; (<a href="http://www.creditcards.com/credit-card-news/credit-card-agreement-readability-1282.php">http://www.creditcards.com/credit-card-news/credit-card-agreement-readability-1282.php</a>)
			used a software program that's commonly used in analyzing how difficult
			or easy it is to read school textbooks to review and rate more than 1,200
			credit card agreements. </p>
			<p>The study found that the average agreement in the United States is written
			at a 12th grade level -- well above the ability of four out of five adults
			to understand what they are reading. The analysis named the 10 most unreadable
			contracts, the 10 easiest to understand and the 10 wordiest. </p>
			<p>The project also compared the average credit card agreement to familiar
			texts, including nursery rhymes, the book of Genesis in the King James
			Bible and the Declaration of Independence. Another component of the package
			was a video (http://www.creditcards.com/credit-card-news/video-credit-card-statement-readability-man-on-street-1282.php)
			that captured the puzzlement of lawyers and college professors as they
			tried to comprehend an obtuse passage from a credit card agreement. </p>
			<p>SABEW judges recognized the readability project for its  &quot;Creative
			use across multiple platforms&quot; in the category for websites with 500,000
			to 2.5 million average unique visitors per month.</p>
			<p>The second SABEW award honored Creditcards.com for online feature writing
			in the same unique monthly visitor category for &quot;<a href="http://www.creditcards.com/credit-card-news/how-to-prepare-inmate-financially-jail-prison-1265.php">Money
			and Prison</a>&quot; (http://www.creditcards.com/credit-card-news/how-to-prepare-inmate-financially-jail-prison-1265.php).
			This package explored what happens to a person's finances once he or she
			becomes incarcerated and offered tips to help minimize the financial hit
			to those facing jail time. It is a topic that isn't often written about,
			but one that is of intense interest to the nearly 2 million incarcerated
			people in the United States and their spouses and loved ones. It is a fascinating
			new topic for the rest of us.</p>
