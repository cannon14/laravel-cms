			<p>Several issuers raised interest rates by 0.25 percent this week. As a result, the national average annual percentage rate rose to 15.13 percent &mdash; a four-year high. The last time average rates were this high was in January 2012, when the national average began the year at 15.14 percent. Since then, average rates have mostly hovered between 14.9 and 15.09 percent. </p>

			<p>This week&apos;s changes are largely due to the Federal Reserve&apos;s December 2015 rate hike. After the Fed increased the federal funds rate by 0.25 percent, most large issuers passed on that increase to cardholders. Since December, U.S. Bank, Wells Fargo, American Express, Bank of America, Citi, Capital One and Discover have all increased APRs on new card offers. </p>

			<p>The CreditCards.com credit card rate survey (permalink: http://www.creditcards.com/rate-report) is conducted weekly, using offer data from leading U.S. card issuers&apos; websites. Introductory offer periods and regular interest rates will vary with applicants' credit quality and issuer risk-based pricing policies.</p>

