						<p>Average rates on new card  offers stayed at 14.87 percent for the third consecutive week. </p>
						<p>Most issuers left credit card  terms unchanged this week. Chase nudged the APR on its Disney Rewards Visa card  from 15.24 percent to 15.99 percent, but the change was too small to affect the  national average.</p>
						<p>The Disney card's higher APR  did cause average rates for some card categories to rise, however. For example,  the average APR for cash-back credit cards rose to its highest level since  CreditCards.com began tracking rates in mid-2007. The average APR for cash-back  cards is now 15.28 percent -- up from 14.84 percent in March 2014. Average APRs  for rewards and balance transfer cards also increased slightly this week.</p>
The CreditCards.com credit card rate survey (permalink: <a href="http://www.creditcards.com/rate-report">http://www.creditcards.com/rate-report</a>) is conducted weekly, using offer data from the  leading U.S. card issuers' websites. Introductory offer periods and regular  interest rates will vary with applicants' credit quality and issuer risk-based  pricing policies.
