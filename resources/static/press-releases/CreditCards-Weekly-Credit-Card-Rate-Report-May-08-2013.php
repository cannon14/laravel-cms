			<p>Chase prompted this week&rsquo;s rate change by increasing the APR  on the Chase Freedom card by 1 percentage point. The cash-back card previously  featured an APR range of 12.99 percent to 22.99 percent. Now, cardholders may  be offered an APR from 13.99 percent to 22.99 percent.</p>
			<p>The higher rate on the Chase Freedom card caused other rate  categories to rise as well. For example, the average APR for rewards credit  cards is now 14.73 percent, up from 14.72 percent. The average APR for balance  transfer cards also rose from 12.59 percent to 12.62 percent.</p>
