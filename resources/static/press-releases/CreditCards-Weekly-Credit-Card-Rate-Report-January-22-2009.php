			<p>AmEx trimmed select APRs several weeks after the Fed lowered its key lending rate to practically zero. Variable rates are generally tied to the prime rate, which itself tracks the federal funds rate set by the Fed. AmEx acknowledged via e-mail that the latest APR changes were based on prime movement.</p>
			<p>No longer able to trim rates, the Fed faces challenges. In a speech last Thursday, San Francisco Fed president Janet Yellen said that the central bank is trying something new to spur lending: buying up the asset-backed securities that underlie credit cards and other loans.</p>
