			<p>The average annual percentage rate  (APR) dropped to 15.14 percent from last week's 15.19 percent.&nbsp; That is down nearly one-tenth of a percentage  point from the national APR average record, 15.22 percent -- set in mid  December.</p>
			<p>U.S. Bank and Chase spurred this  week's decrease. U.S. Bank changed the low end of the APR range offered for its  U.S. Bank Visa Platinum card.&nbsp; The card  had carried an interest rate of 11.99 percent to 23.99 percent before dropping  to 9.99 percent to 23.99 percent. </p>
			<p>Chase lowered the APR for its Freedom  card from17.99 percent to 14.99 percent.&nbsp;  The change comes in the wake of an overhaul to most of Chase's business  and personal credit lines throughout December.</p>
			<p>Steve O'Halloran, Chase's Public  Affairs Director, confirmed the newest decrease.</p>

