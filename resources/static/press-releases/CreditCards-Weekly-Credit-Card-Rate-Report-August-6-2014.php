			<p>The national average annual  percentage rate (APR) remained stuck at 15.03 percent Wednesday for the fifth  consecutive week.   </p>
			<p>Most issuers left credit card  terms alone this week. The sporting goods store Cabela's fine-tuned the APR on  the Cabela's Club Visa by .01 percent. But the change was so small, it didn't  affect the national average.</p>
			<p>Unlike most U.S. credit  cards, the Cabela's Club Visa is tied to the 1-month Libor rate, which increased  this week from 0.15 percent to 0.16 percent. As a result, the APR on the  Cabela's Club Visa inched up as well.</p>
			<p>Frequent Cabela's shoppers  who apply for the store card will now be offered an APR as low as 15.15 percent  for non-store purchases or as high as 21.15 percent.</p>
