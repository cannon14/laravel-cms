        <h1>FAQ</h1>
        <hr size="1" color="#cccccc" />
        
                <b>What is it?</b>
		<p>A live, interactive town hall discussion of credit scoring and credit reporting and how it impacts you. The forum, hosted by Credit Cards.com and credit bureau Experian will feature Rod Griffin, Experian's Director of Public Education.</p>
		
		<b>When is it? </b>
		<p>Thursday, May 6, 2010 at 2 p.m. Eastern/11 a.m. Pacific.</p>
		
		<b>What will happen during the town hall?</b>
		<p>Rod Griffin, Experian's Director of Public Education, will answer participant questions via live video stream -- direct from the CreditCards.com studios in Austin, Texas. While the video stream is happening, you'll be able to submit your own questions or comments, through a form at http://www.CreditCards.com/askexperian or through Twitter at hashtag #askexperian. You can also submit questions in advance of the event, via the same Twitter hashtag or the form on the page www.CreditCards.com/AskExperian.</p>
                <p>CreditCards.com will choose which questions to answer from those submitted before and during the event. Because a large volume of questions is expected, there is no guarantee that your question will be answered during the event.</p>
                
                <a name="griffin"></a>

                <b>Who will be answering the questions?</b>
                <p>Answering credit reporting and credit scoring questions from the White House will be Rod Griffin, Director of Public Education for Experian. He is responsible for Experian's national consumer education program and supports the company's community involvement and corporate responsibility efforts.</p>
                <p>Griffin speaks regularly at regional and national financial literacy events and supports various national consumer education initiatives including the LifeSmarts Consumer Knowledge Competition, for which he serves on the Corporate Advisory Board, and the Jumpstart Coalition for Financial Literacy with the goal of promoting financial education for America's youth. Griffin holds a Fair Credit Reporting Act certification from the Consumer Data Industry Association.</p>

                <b>I have a problem with my own credit report or score. Will he be able to straighten it out?</b>
                <p>Sorry, but no. Use Experian's website to seek individual answers. We will not have access to individuals' information and can answer only questions that apply to more than one person.</p>

		<b>Do I have to login to view or participate in the town hall?</b>
		<p>No. You can view and submit comments without logging in. If you choose to login, you can do so using your logins from Facebook, Twitter or MySpace. However, there is no requirement to log in.</p>
		
		<b>Do I need any software downloads to view or participate in the town hall?</b>
		<p>The video feed runs uses Flash software; all but the oldest computers and oldest Web browsers can handle Flash. Put it this way: If you can watch a video on YouTube, you can watch this. Want the latest version of Flash? Go to <a href="http://get.adobe.com/flashplayer/" target="_blank">http://get.adobe.com/flashplayer/</a>.</p>
		
