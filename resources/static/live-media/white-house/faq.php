        <h1>FAQ</h1>
        <hr size="1" color="#cccccc" />
        
        <b>What is it?</b>
		<p>A live, interactive town hall discussion of credit card reform and how it will impact you. The forum, hosted by Credit Cards.com, will feature Austan Goolsbee, a member of the President's Council of Economic Advisers, and will focus on the Credit CARD Act of 2009 -- the most sweeping changes in the history of the credit card business.</p>
		
		<b>When is it? </b>
		<p>Monday, February 22, 2010 at 2 p.m. Eastern/11 a.m. Pacific.</p>
		
		<b>What will happen during the town hall?</b>
		<p>Austan Goolsbee, a member of the President's Council of Economic Advisers, will answer participant questions via live video stream -- direct from the White House. While the video stream is happening, you'll be able to submit your own questions or comments, through a form at http://www.CreditCards.com/askthewhitehouse or through Twitter at hashtag #cardlaw. Goolsbee will choose which questions to answer from those submitted before and during the event. Because a large volume of questions is expected, there is no guarantee that your question will be answered during the event.</p>
		
		<b>Who will be answering the questions?</b>
		<p>Answering credit card reform questions from the White House will be Austan Goolsbee, a member of the President's Council of Economic Advisers.</p>
		
		<b>Do I have to login to view or participate in the town hall?</b>
		<p>No. You can view and submit comments without logging in. If you choose to login, you can do so using your logins from Facebook, Twitter or MySpace. However, there is no requirement to log in.</p>
		
		<b>Do I need any software downloads to view or participate in the town hall?</b>
		<p>The video feed runs uses Flash software; all but the oldest computers and oldest Web browsers can handle Flash. Put it this way: If you can watch a video on YouTube, you can watch this. Want the latest version of Flash? Go to <a href="http://get.adobe.com/flashplayer/" target="_blank">http://get.adobe.com/flashplayer/</a>.</p>
		
		<b>How can I ask a question?</b>
		<p>Before the event:
		<ul>
            <li>Fill out comment form at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a> . No login required.</li>
		    <li>Type your question into Facebook field at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a>. You must use your Facebook login to comment this way.</li>
		    <li>Submit a question through Twitter using hashtag #cardlaw. You must login to your Twitter account to send your comment this way.</li>
		</ul>
		</p>
		<p>During the event:
		<ul>
            <li>Fill out the comment form at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a> . No login required.</li>
		    <li>Submit a question through Twitter using hashtag #cardlaw. You must login to your Twitter account to send your comment this way.</li>
		</ul>
		</p>
		
		<b>Can I submit a question through Facebook?</b>
		<p>No. However, in the days leading up to the event, there will be a Facebook box at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a> in which you submit your questions or comments to be included in the town hall. (You must be logged in to Facebook to leave your comment.) Any posts you make in this box will then appear on your Facebook page as a status update. </p>
		
		<b>Can I submit a question through Twitter?</b>
		<p>Yes. Just be sure to include the hashtag #cardlaw in your Tweet, and we will be able to access your question.</p>
		
		<b>Will all questions be answered?</b>
		<p>Maybe. Because a large volume of questions is likely, there is no guarantee that your question will be answered during the event.</p>
		
		<b>Can I get a reminder of the event?</b>
		<p>Yes, click on the "Get a reminder" tab at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a>. That will take you to a page where you can give us your e-mail address to which you'd like the reminder sent and tell us how soon before the event you'd like to be reminded. Then, at the time you requested, you'll receive an e-mail with all the important information about the event -- including a direct link to the town hall main page.</p>
		
		<b>Can I invite a friend?</b>
		<p>Yes. Click on the "Invite a friend" link at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a>. That will open up a new e-mail in your default e-mail program that includes information about the event and a link to the main town hall page. Then, just add in as many e-mail addresses as you like and send it on.</p>
		
		<b>Where can I learn more about credit card reform?</b>
		<p>Visit our special section on <a href="/reform" target="_blank">credit card reform</a>. It's filled with news updates, videos, tips, timelines and everything you need to know leading up to Feb. 22.</p>
		
		<b>What is CreditCards.com? </b>
		<p>CreditCards.com is the leading online credit card marketplace connecting consumers with multiple credit card issuers. CreditCards.com enables consumers to search for, compare and apply for credit cards and offers credit card advice, news, statistics and tools.  In 2009, more than 12 million consumers used CreditCards.com to make smart choices about credit cards.</p>
		
		<b>Will the town hall be archived?</b>
		<p>Following the Town Hall, the chat video and a transcript of the video will be posted at <a href="/askthewhitehouse" target="_blank">http://www.CreditCards.com/askthewhitehouse</a>, as will a complete transcript of the chat text.</p>
        
