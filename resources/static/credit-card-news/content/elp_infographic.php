<h2>Survey: Bad financial habits creeping back</h2>


<p>
Are we creeping back into our bad, old financial habits? A survey released Monday suggests so: The number of Americans who say they don't prepare a budget is rising, while the number cutting their spending is falling.
</p>
<p>
According to the <a href="http://www.nfcc.org/NewsRoom/FinancialLiteracy/files2013/NFCC_2014%20FinancialLiteracySurvey_FINAL.pdf" target="_blank">2014 Consumer Financial Literacy Survey</a>, three-fifths of adults in the U.S. say they do not have a budget. The survey was commissioned by the National Foundation for Credit Counseling.
</p>
<p>
Harris Poll conducted this poll online from March 4-6, surveying 2,016 people who were 18 and older. Figures are weighted where necessary to align them with the full U.S. population. A unit of the credit reporting bureau Experian sponsored this year's survey. 
</p>

